void main() {
  // Variable initialization
  var num = 2 + 2; // num is 4

  // -------------------------
  // Conditional Statements (if-else)
  // -------------------------
  if (num == 4) {
    print("if 4"); // Will print because num is 4
  } else if (num == 3) {
    print("else if 3"); // Will not print
  } else if (num == 2) {
    print("else if 2"); // Will not print
  } else {
    print("else"); // Will not print
  }
}
